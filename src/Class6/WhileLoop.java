package Class6;

public class WhileLoop {
    public static void main(String[] args) {
        int number=1;
        while (number<=100){
            System.out.println(number);
            number++;
        }
        number=1;
        while (number<=15){
            System.out.println(number);
            number+=2;
        }
        int number1=5;
        while (number1<=50){
            System.out.println(number1);
            number1+=5;

        }
        int number2=5;
        while (number2<=25){
            System.out.println(number2);
            number2+=5;
        }

    }
}
