package Class7;

public class ForLoops {
    public static void main(String[] args) {

        int number=0;
        while (number<10){
            System.out.println(number);
            number++;
        }

        for (int i=0;i<10;i++){
            System.out.println(i);

        }
        for (int i=1;i<20;i+=2){
            System.out.println(i);
        }



    }
}
